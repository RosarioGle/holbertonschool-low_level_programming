#include "3-calc.h"

/**
 * op_add - do the sum of the two numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - do the substraction of the two numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the substraction of a by b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - do the multiplication of a and b
 *
 * @a: the first number
 * @b: thesecond number
 *
 * Return: the multiplication of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - do the divition of the two numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the division of a by b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - do the modulo of the two numbers
 *
 * @a: the first number
 * @b: the second number
 *
 * Return: the modulo of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
